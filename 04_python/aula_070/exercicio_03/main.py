#Importações
import math
import random





# Variáveis
exemplo_int = -15
exemplo_float = -12.35





# Executar
print("\n\n")
print("Int original: (", exemplo_int, ")")
print("Float original: (", exemplo_float, ")\n")

print("Valor absoluto do Int: (", abs(exemplo_int), ")")
print("Valor absoluto do Float: (", abs(exemplo_float), ")\n")

print("Arredondar segundo as regras matemáticas: (", round(exemplo_float), ")")
print("Arredondar sempre para cima: (", math.ceil(exemplo_float), ")")
print("Arredondar sempre para baixo: (", math.floor(exemplo_float), ")\n")

print("Numero aleatório entre 6 e 12: (", random.randint(6, 12), ")\n")

print("\n\n")
# Exemplo sintático de uma função: print(variavel)
# Exemplo sintático de um método: variavel.print()


print("\n\n")


# Variáveis
exemplo_string = ("Boa noite Codemaster")

# Executar
print("String original: (", exemplo_string, ")\n")

print("String caixa baixo: (", exemplo_string.lower(), ")")
print("String caixa alta: (", exemplo_string.upper(), ")")
print("String capitalize: (", exemplo_string.capitalize(), ")")
print("String title: (", exemplo_string.title(), ")\n")

print("Total de vezes que ocorreu o caracter 'o': (", exemplo_string.count("o"), ")")
print("Total de caracteres na string inteira: (", len(exemplo_string), ")\n")

print("Substituir uma letra ou palavra: (", exemplo_string.replace("o", "#"), ")")


print("\n\n")